package DSA_Programs.SpaceComplexity;

public class SpaceComplexities {
    public static void main(String[] args)
    {
        // Space Complexity = 0(1)
        for(int i=1;i<10;i++)
        {
            System.out.println("TimeComplexity");
        }
        // Time Complexity = 0(1)
        int n=5;
        for(int i=1;i<n;i++)
        {
            for(int j=1;j<n;j++)
            {
                System.out.println("TimeComplexity");
            }

        }
    }
}
