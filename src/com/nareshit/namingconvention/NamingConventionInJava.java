
//A package should be written in lowercase, usually the reverse of the company name.
package com.nareshit.namingconvention;

// While writing a class in Java, use Pascal naming convention.
class ThisIsExampleOfClass{

    //While writing a method in Java, use camel case naming convention.
    public void thisIsExampleOfMethod(){

        //While writing a variable in Java, use camel case naming convention.
        String studentName;
        int rollNumber;

        //While writing the final variable in Java, use snake_case naming convention.
        final int MIN_Value;
        final int MAX_Value;

    }

}

// Main Method Class
public class NamingConventionInJava{
    public static void main(String[] args) {
        ThisIsExampleOfClass example=new ThisIsExampleOfClass();
        example.thisIsExampleOfMethod();

    }
}
